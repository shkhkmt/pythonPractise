# Printing questions and taking inputs in the same line of code. 

age = input("How old are you? ")
height = input("How tall are you? ")
weight = input("How much do you weigh? ")

# Printing variables to string with formatter. 
# If you exclude the 'f' the string will print out the names of the variables in the braces.

print(f"So, you're {age} old, {height} tall and, {weight} heavy")